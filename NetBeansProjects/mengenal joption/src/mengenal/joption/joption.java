    private void btnConfirmDilalogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmDilalogActionPerformed
        // TODO add your handling code here:
        int jawab = JOptionPane.showConfirmDialog(this, "silahkan konfirmasi!!");
        
//        0 = yes
//        1 = no
//        2 = cancel
                
        switch(jawab){
            case JOptionPane.YES_OPTION:
                 JOptionPane.showMessageDialog(this, "kamu menjawab ya");
            break;
            case JOptionPane.NO_OPTION:
                 JOptionPane.showMessageDialog(this, "kamu menjawab tidak");
                 break;
            case JOptionPane.CANCEL_OPTION:
                JOptionPane.showMessageDialog(this, "kamu menjawab batal");
                break;
            default:
                JOptionPane.showMessageDialog(this, "kamu tidak melakukan apa apa");
                break;
        }
    }//GEN-LAST:event_btnConfirmDilalogActionPerformed

    private void btnOptionDialogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOptionDialogActionPerformed
        // TODO add your handling code here:
         
        int jawwab = JOptionPane.showOptionDialog(this, 
                "ingin keluar ?",
                "keluar",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                null,
                null);
        
        if (jawwab == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, "program akan keluar!!");
            System.exit(0);
        }
    }//GEN-LAST:event_btnOptionDialogActionPerformed

