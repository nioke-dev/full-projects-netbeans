    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        String isbn = txt_isbn.getText();
        String judul = txt_judul.getText();
        String penerbit = txt_penerbit.getText();
        String jumlah = txt_jumlah.getText();
        try {
            conn = koneksi.koneksi();
            String sql = "update buku set judul = ?,penerbit = ?,jumlah = ? where isbn = ? ";            
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, judul);
            pst.setString(2, penerbit);
            pst.setString(3, jumlah);
            pst.setString(4, isbn);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Update data success");
            getDataSiswa();
            reset();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Upadate Data Gagal" + e.getMessage());
        }
    }//GEN-LAST:event_btn_updateActionPerformed

