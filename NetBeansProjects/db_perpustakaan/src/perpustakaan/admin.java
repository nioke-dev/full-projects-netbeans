    private void btn_createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_createActionPerformed
        // TODO add your handling code here:
        String isbn = txt_isbn.getText();
        String judul = txt_judul.getText();
        String penerbit = txt_penerbit.getText();
        String jumlah = txt_jumlah.getText();
        try {
            String sql = "insert into buku (isbn,judul,penerbit,jumlah) values(?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, isbn);
            pst.setString(2, judul);
            pst.setString(3, penerbit);
            pst.setString(4, jumlah);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Create Data Success");
            getDataSiswa();
            reset();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Create Data Gagal" + e.getMessage());
        }
    }//GEN-LAST:event_btn_createActionPerformed

    private void tabel_perpusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_perpusMouseClicked
        // TODO add your handling code here:
        //select rownya
        int baris  = tabel_perpus.getSelectedRow();        
        //ambil value dari isbn
        String isbn = model.getValueAt(baris, 0).toString();
        try {
            String sql = "select * from buku where isbn = '" + isbn + "'";        
            //buat statement
            st = conn.createStatement();            
            //tampung si statement di result set
            rs = st.executeQuery(sql);
            //jika si result set ada maka lanjutkan
            if (rs.next()){
                //select si textfield terus settext yang ada di result set
                //dengan cara mengambil data yang ada di resultset sesuai dengan yang ada di textfield
                //kemudian transfer ke textfield
                txt_isbn.setText(rs.getString("isbn"));
                txt_judul.setText(rs.getString("judul"));
                txt_penerbit.setText(rs.getString("penerbit"));
                txt_jumlah.setText(rs.getString("jumlah"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "mouse clicked gagal" + e.getMessage());
        }
    }//GEN-LAST:event_tabel_perpusMouseClicked

