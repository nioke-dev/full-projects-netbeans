    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        PreparedStatement ps;
        ResultSet rs;
        
        
        String uname = String.valueOf(jTextField_Username.getText());        
        String pass = String.valueOf(jPasswordField_Password.getPassword());
       
        
        String query = "SELECT * FROM `the_app_users` WHERE `u_uname`=? AND `u_pass` =?";
        
        
        try {
            ps = MyConnection.Koneksi().prepareStatement(query);
            
            ps.setString(1, uname);
            ps.setString(2, pass);
            rs = ps.executeQuery();
            
            
            if (rs.next()) {
                
                HOME_JFRAME mf = new HOME_JFRAME();
                mf.setVisible(true);
                mf.setLocationRelativeTo(null);
                mf.setExtendedState(JFrame.MAXIMIZED_BOTH);
                mf.jLabel1.setText("welcome<" + uname+">");
            }else{
                JOptionPane.showMessageDialog(null, "Incorrect Username or Password", "Login Failed",JOptionPane.WARNING_MESSAGE);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

